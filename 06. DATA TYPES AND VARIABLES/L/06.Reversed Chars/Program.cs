using System;

namespace _06.Reversed_Chars
{
    class Program
    {
        static void Main(string[] args)
        {
            var a = char.Parse(Console.ReadLine());
            var b = char.Parse(Console.ReadLine());
            var c = char.Parse(Console.ReadLine());

            Console.WriteLine($"{c} {b} {a}");
        }
    }
}
